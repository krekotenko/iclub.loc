			$data['reward'] = $product_info['reward'];
			$data['points'] = $product_info['points'];
			$data['description'] = html_entity_decode($product_info['description'], ENT_QUOTES, 'UTF-8');

			if ($product_info['stock_status']) {
				$data['stock'] = $product_info['stock_status'];
			}
			else {
				$data['stock'] = $this->language->get('text_instock');
			}

			$data['isStock'] = true;
			if($data['stock'] != "Есть в наличии") {
                $data['isStock'] = false;
            }

			$this->load->model('tool/image');

			if ($product_info['image']) {
				$data['popup'] = $this->model_tool_image->resize($product_info['image'], 97, 97);
			} else {
				$data['popup'] = '';
			}

			if ($product_info['image']) {
				$data['thumb'] = $this->model_tool_image->resize($product_info['image'], 430,430);
				$this->document->setOgImage($data['thumb']);
			} else {
				$data['thumb'] = '';
			}

			$data['images'] = array();

			$results = $this->model_catalog_product->getProductImages($this->request->get['product_id']);

			foreach ($results as $result) {
				$data['images'][] = array(
					'popup' => $this->model_tool_image->resize($result['image'], $this->config->get($this->config->get('config_theme') . '_image_popup_width'), $this->config->get($this->config->get('config_theme') . '_image_popup_height')),
					'thumb' => $this->model_tool_image->resize($result['image'], $this->config->get($this->config->get('config_theme') . '_image_additional_width'), $this->config->get($this->config->get('config_theme') . '_image_additional_height'))
				);
			}

			if ($this->customer->isLogged() || !$this->config->get('config_customer_price')) {
				$data['price'] = $this->currency->format($this->tax->calculate(($product_info['price'] * $this->config->get('config_usd')), $product_info['tax_class_id'], $this->config->get('config_tax')), $this->session->data['currency']);
                //var_dump($product_info['price'] * $this->config->get('config_usd'));
                //exit();
			} else {
				$data['price'] = false;
			}

			if ($product_info['price_discount'] && ($this->customer->isLogged() || !$this->config->get('config_customer_price'))) {
				$data['price_discount'] = $this->currency->format($this->tax->calculate((($product_info['price'] - (($product_info['price_discount']*$product_info['price']) / 100)) * $this->config->get('config_usd')), $product_info['tax_class_id'], $this->config->get('config_tax')), $this->session->data['currency']);
			} else {
				$data['price_discount'] = false;
			}



			if ($product_info['special']) {
                $specialDiscount = 0;
                $textSpecial = "";
                $date_endSpecial = "";
                list($specialDiscount, $textSpecial, $date_endSpecial) = explode('|',$product_info['special']);

				$data['special'] = $this->currency->format($this->tax->calculate((($product_info['price'] - (($specialDiscount*$product_info['price']) / 100)) * $this->config->get('config_usd')), $product_info['tax_class_id'], $this->config->get('config_tax')), $this->session->data['currency']);

				$data['textSpecial'] = $textSpecial;
				$data['date_endSpecial'] = $date_endSpecial;
			} else {
				$data['special'] = false;
			}

			$data['is_special'] = true;
			if(!$data['special']) {
                $data['special'] = $data['price_discount'];
                $data['is_special'] = false;
            }

			$discounts = $this->model_catalog_product->getProductDiscounts($this->request->get['product_id']);

			$data['discounts'] = array();

			foreach ($discounts as $discount) {
				$data['discounts'][] = array(
					'quantity' => $discount['quantity'],
					'price'    => $this->currency->format($this->tax->calculate($discount['price'], $product_info['tax_class_id'], $this->config->get('config_tax')), $this->session->data['currency'])
				);
			}

			$data['options'] = array();

			foreach ($this->model_catalog_product->getProductOptions($this->request->get['product_id']) as $option) {
				$product_option_value_data = array();

				foreach ($option['product_option_value'] as $option_value) {
					if (!$option_value['subtract'] || ($option_value['quantity'] > 0)) {
						if ((($this->config->get('config_customer_price') && $this->customer->isLogged()) || !$this->config->get('config_customer_price')) && (float)$option_value['price']) {
							$price = $this->currency->format($this->tax->calculate($option_value['price'], $product_info['tax_class_id'], $this->config->get('config_tax') ? 'P' : false), $this->session->data['currency']);
						} else {
							$price = false;
						}

						$product_option_value_data[] = array(
							'product_option_value_id' => $option_value['product_option_value_id'],
							'option_value_id'         => $option_value['option_value_id'],
							'name'                    => $option_value['name'],
							'image'                   => $option_value['image'] ? $this->model_tool_image->resize($option_value['image'], 50, 50) : '',
							'price'                   => $price,
							'price_prefix'            => $option_value['price_prefix']
						);
					}
				}

				$data['options'][] = array(
					'product_option_id'    => $option['product_option_id'],
					'product_option_value' => $product_option_value_data,
					'option_id'            => $option['option_id'],
					'name'                 => $option['name'],
					'type'                 => $option['type'],
					'value'                => $option['value'],
					'required'             => $option['required']
				);
			}

			if ($product_info['minimum']) {
				$data['minimum'] = $product_info['minimum'];
			} else {
				$data['minimum'] = 1;
			}

			$data['review_status'] = $this->config->get('config_review_status');

			if ($this->config->get('config_review_guest') || $this->customer->isLogged()) {
				$data['review_guest'] = true;
			} else {
				$data['review_guest'] = false;
			}

			if ($this->customer->isLogged()) {
				$data['customer_name'] = $this->customer->getFirstName() . '&nbsp;' . $this->customer->getLastName();
			} else {
				$data['customer_name'] = '';
			}


