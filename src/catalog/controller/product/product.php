	public function review() {
		$this->load->language('product/product');

		$this->load->model('catalog/review');
		$this->load->model('catalog/product');
		$this->load->model('catalog/category');

		$data['text_no_reviews'] = $this->language->get('text_no_reviews');

		$limit = 3;
		$offset = 0;

		if(isset($this->request->get['next'])) {
            $limit = 184467440737;
            $offset = 3;
        }
		$data['reviews'] = array();


        $productCategories = $this->model_catalog_product->getCategories($this->request->get['product_id']);
        if($productCategories) {
            $productCategory = array_filter($productCategories,function ($item) {
                if($item['main_category']) {
                    return $item;
                }
            });
        }
        if(isset($productCategory[0])) {
            $resultReviews = $this->model_catalog_category->getCategoryReviews($productCategory[0]['category_id']);
        }

		$results = $this->model_catalog_review->getReviewsByProductId($this->request->get['product_id'], $offset, $limit);

        foreach ($results as $result) {

            $paramReview = [];
            $tmpValue = 0;
            $i = 0;
		    foreach ($resultReviews as $item) {
                $reviewContent = $this->model_catalog_category->getCategoryReviewContentByreview($item['id'], $this->request->get['product_id'], $result['review_id']);

                $valueSumm = 0;
                foreach($reviewContent as $v) {
                    $valueSumm += (int)$v['value'];
                }
                $value = round(count($reviewContent) > 0 ? $valueSumm/count($reviewContent) : 0, 1);
                $paramReview[] = [
                    'id' => $item['id'],
                    'name' => $item['name'],
                    'value' => $value
                ];
                if($value > 0) {
                    $i++;
                }
                $tmpValue += $value;
		    }

			$data['reviews'][] = array(
				'author'     => $result['author'],
				'text'       => nl2br($result['text']),
				'rating'     => (int)$result['rating'],
				'paramReview' => $paramReview,
                'srReviews' => round(($tmpValue / $i),1),
				'date_added' => $this->rel_date($result['date_added'])
			);
		}

		$this->response->setOutput($this->load->view('product/review', $data));
	}

