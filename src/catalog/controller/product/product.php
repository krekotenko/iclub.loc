            $data['product_reviews'] = array();
            $data['srReviews'] = 0;
			$data['totalReviews'] = $this->model_catalog_review->getTotalReviewsByProductId($this->request->get['product_id']);

            $resultReviews = null;
            $productCategories = $this->model_catalog_product->getCategories($product_id);
            if($productCategories) {
                $productCategory = array_filter($productCategories,function ($item) {
                    if($item['main_category']) {
                        return $item;
                    }
                });
            }

            if(isset($productCategory[0])) {
                $resultReviews = $this->model_catalog_category->getCategoryReviews($productCategory[0]['category_id']);
            }

            if($resultReviews) {
                $tmpValue = 0;
                $i = 0;
                foreach ($resultReviews as $review)
                {
                    $reviewContent = $this->model_catalog_category->getCategoryReviewContent($review['id'], $this->request->get['product_id']);
                    $valueSumm = 0;
                    foreach($reviewContent as $item) {
                        $valueSumm += (int)$item['value'];
                    }

                    $value = round(count($reviewContent) > 0 ? $valueSumm/count($reviewContent) : 0, 1);
                    if($value > 0) {
                        $i++;
                    }
                    $data['product_reviews'][] = [
                        'id' => $review['id'],
                        'name' => $review['name'],
                        'value' => $value
                    ];


                    $tmpValue += $value;
                }
            }
            if(isset($i) && $i) {
                $data['srReviews'] = round(($tmpValue / $i),1);
            }

